
// note we dont need to import anything 
// note all results are in double by default  except a few
public class math {
    public static void main(String[] args){
   System.out.println(Math.PI); // give pi valuse

   System.out.println(Math.E); // gives eulers number

   System.out.println( Math.pow(2,3)); // 2**3 in double 

   System.out.println(Math.abs(-5)); // give the absolute value

   System.out.println(Math.sqrt(9)); // give the square root

   System.out.println(Math.round(3.14)); // rounds to the nearest integer 

   System.out.println(Math.ceil(3.14)); // rounds off to larger integer

   System.out.println(Math.floor(3.99)); // rounds down to integer

   System.out.println(Math.max(10,20));// prints the maximum

   System.out.println(Math.min(20,30 )); // prints minimum

   float ans=5/3;
   System.out.printf("The value is %.2f",ans);
    }
}
