public class arithmetic {
    public static void main(String[] args){
        int x=10;
        int y=3;
        int z;

        z=x+y;
        System.out.println(z);
        z=x-y;
        System.out.println(z);
        z=x*y;
        System.out.println(z);
        z= x/y;
        System.out.println(z);
        z= x%y;
        System.out.println(z);
        
        // order of operations [P-E-M-D-A-S]
    }
}
